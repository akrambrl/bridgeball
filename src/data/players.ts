// Generate Le Pont pairs from the unified player list
export function generatePontDB(players: PlayerEntry[]) {
  const db: Record<string, Array<{ c1: string; c2: string; p: string[]; h: string }>> = {
    facile: [], moyen: [], expert: [],
  };

  const pairMap: Record<string, { players: string[]; diff: "facile" | "moyen" | "expert" }> = {};

  for (const player of players) {
    if (player.clubs.length < 2) continue;
    // Only use clubs that are in the PONT_CLUBS set
    const bigClubs = player.clubs.filter(c => PONT_CLUBS.has(c));
    if (bigClubs.length < 2) continue;
    for (let i = 0; i < bigClubs.length; i++) {
      for (let j = i + 1; j < bigClubs.length; j++) {
        const [a, b] = [bigClubs[i], bigClubs[j]].sort();
        const key = `${a}|||${b}`;
        if (!pairMap[key]) {
          pairMap[key] = { players: [], diff: player.diff };
        }
        if (!pairMap[key].players.includes(player.name)) {
          pairMap[key].players.push(player.name);
        }
        const order = { facile: 0, moyen: 1, expert: 2 };
        if (order[player.diff] < order[pairMap[key].diff]) {
          pairMap[key].diff = player.diff;
        }
      }
    }
  }

  for (const [key, val] of Object.entries(pairMap)) {
    const [c1, c2] = key.split("|||");
    db[val.diff].push({ c1, c2, p: val.players, h: "" });
  }

  for (const diff of ["facile", "moyen", "expert"] as const) {
    db[diff].sort(() => Math.random() - 0.5);
  }

  return db;
}
